#include "libft.h"

int		ft_wcharlen(wchar_t wc)
{
	RET_IF(wc < 0x80, 1);
	RET_IF(wc < 0x800, 2);
	RET_IF(wc < 0x10000, 3);
	RET_IF(wc < 0x110000, 4);
	return (0);
}
